x = True       # 변수 x에 True 값을 할당한다.
y = False      # 변수 y에 True 값을 할당한다.
print(x == y)  # x와 y가 같은 값인지 확인한다.
print(x > y)   # x가 y보다 큰 값인지 확인한다.
print(x <= y)  # x가 y보다 작거나 같은 값인지 확인한다.

